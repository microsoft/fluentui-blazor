namespace Microsoft.FluentUI.AspNetCore.Components;

/// <summary>
/// The visual appearance of a component.
/// </summary>
public enum Appearance
{
    /// <summary>
    /// Neutral appearance.
    /// </summary>
    Neutral,

    /// <summary>
    /// Uses the current accent color.
    /// </summary>
    Accent,

    /// <summary>
    /// Shown as a link.
    /// </summary>
    Hypertext,

    /// <summary />
    Lightweight,

    /// <summary>
    /// Show a border.
    /// </summary>
    Outline,

    /// <summary>
    /// Reveal on hover
    /// </summary>
    Stealth,

    /// <summary>
    /// Show filled.
    /// </summary>
    Filled
}
