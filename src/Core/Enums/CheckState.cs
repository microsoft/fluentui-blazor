namespace Microsoft.FluentUI.AspNetCore.Components;

/// <summary>
/// Different states for the <see cref="FluentCheckbox"/> component.
/// </summary>
public enum CheckState
{
    /// <summary>
    /// The <see cref="FluentCheckbox"/> displays a check mark.
    /// </summary>
    Checked,

    /// <summary>
    /// The <see cref="FluentCheckbox"/> is empty.
    /// </summary>
    Unchecked,

    /// <summary>
    /// The <see cref="FluentCheckbox"/> displays a indeterminate check mark.
    /// </summary>
    Indeterminate
}
