namespace Microsoft.FluentUI.AspNetCore.Components;

/// <summary>
///  Defines the possible options for mouse buttons. 
/// </summary>
public enum MouseButton
{
    /// <summary>
    /// No button.
    /// </summary>
    None = -1,

    /// <summary>
    /// Left button.
    /// </summary>
    Left = 0,

    /// <summary>
    /// Middle button.
    /// </summary>
    Middle = 1,

    /// <summary>
    /// Right button.
    /// </summary>
    Right = 2,

    /// <summary>
    /// X1 button (back).
    /// </summary>
    Back = 3,

    /// <summary>
    /// X2 button (forward).
    /// </summary>
    Forward = 4
}
