namespace Microsoft.FluentUI.AspNetCore.Components;

/// <summary>
/// Describes the horizontal positioning of a <see cref="FluentAnchoredRegion"/>.
/// </summary>
public enum HorizontalPosition
{
    /// <summary>
    /// No positions set.
    /// </summary>
    Unset,

    /// <summary>
    /// Position at the start of the anchor.
    /// </summary>
    Start,

    /// <summary>
    /// Position at the end of the anchor.
    /// </summary>
    End,

    /// <summary>
    /// Position on the left of the anchor.
    /// </summary>
    Left,

    /// <summary>
    /// Position on the right of the anchor.
    /// </summary>
    Right,

    /// <summary>
    /// Position at the center of the anchor.
    /// </summary>
    Center,
}
