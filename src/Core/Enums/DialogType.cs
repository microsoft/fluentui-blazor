namespace Microsoft.FluentUI.AspNetCore.Components;

/// <summary>
/// The type of <see cref="FluentDialog"/>.
/// </summary>
public enum DialogType
{
    /// <summary>
    /// A normal dialog.
    /// </summary>
    Dialog,

    /// <summary>
    /// A dialog shown as a message box.
    /// </summary>
    MessageBox,

    /// <summary>
    /// A dialog shown as a panel.
    /// </summary>
    Panel,

    /// <summary>
    /// A dialog shown as a splash screen.
    /// </summary>  
    SplashScreen
}
