namespace Microsoft.FluentUI.AspNetCore.Components;

/// <summary>
/// Describes the vertical positioning of a <see cref="FluentAnchoredRegion"/>.
/// </summary>
public enum VerticalPosition
{
    /// <summary>
    /// No position set.
    /// </summary>
    Unset,

    /// <summary>
    /// Position at the top.
    /// </summary>
    Top,

    /// <summary>
    /// Position at the bottom.
    /// </summary>
    Bottom,

    /// <summary>
    /// Position at the center.
    /// </summary>
    Center
}
